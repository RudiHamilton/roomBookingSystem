    private Room findRoomByID(String roomID) {
        ArrayList<Room> allRooms = new ArrayList<>();
        allRooms.addAll(AppLib.labRooms);
        allRooms.addAll(AppLib.lectureHalls);
        for (Room room : allRooms) {
            if (room.RoomIDGet().equals(roomID)) {
                allRooms.clear();
                return room;
            }
        }
        return null;
    }
