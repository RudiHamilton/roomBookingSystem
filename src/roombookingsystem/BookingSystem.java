    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int i = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this booking?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        switch (i) {
            case JOptionPane.YES_OPTION -> {
                String BookingIDSearch = (String) jTextField5.getText();
                boolean bookingFound = false;
                Iterator<Booking> iterator = AppLib.booked.iterator();
                System.out.println(i);
                while (iterator.hasNext()) {
                    Booking booking = iterator.next();
                    if (booking.BookingIDGet().equals(BookingIDSearch)) {
                        iterator.remove();
                        jLabel28.setVisible(true);
                        jLabel28.setText("Record was deleted successfully.");
                        bookingFound = true;
                        
                        break;
                    }
                }
            }
            case JOptionPane.NO_OPTION -> {
                jLabel28.setVisible(true);
                jLabel28.setText("Record wasnt deleted, user aborted.");
            }
            default -> {
                jLabel28.setVisible(true);
                jLabel28.setText("Record wasnt deleted, user aborted.");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String time = (String) jComboBox1.getSelectedItem();
        String contactEmail = jTextField1.getText();
        String contactPerson = jTextField3.getText();
        String roomID = (String) jTextField4.getText();
        String phoneNumber = jTextField2.getText();
        String date = jLabel5.getText();

        Room room = findRoomByID(roomID);

        if (room != null) {
            // Check if the room is available at the given time on the given date
            boolean isTimeAvailable = true;
    
    // Define the time slots
            String[] timeSlots = {
                "09:00 - 11:00",
                "11:00 - 13:00",
                "13:00 - 15:00",
                "15:00 - 17:00",
                "09:00 - 13:00",
                "13:00 - 17:00",
                "09:00 - 17:00"
            };
            
            // Check if the selected time slot is already booked for the given room on the given date
            for (Object obj : AppLib.booked) {
                Booking booking = (Booking) obj;
                if (booking.RoomGet().RoomIDGet().equals(roomID) && booking.DateGet().equals(date) && booking.TimeGet().equals(time)) {
                    isTimeAvailable = false;
                    break;
                }
            }
            if (isTimeAvailable) {
                count = count + 1;
                String bookingID = Integer.toString(count);
                Booking newBooking = new Booking(bookingID, date, time, contactPerson, contactEmail, phoneNumber, room);
                AppLib.booked.add(newBooking);
                jLabel29.setVisible(true);
                jLabel29.setText("Your booking was successfully created. Booking ID is: " + count);
            } 
            else {
                jLabel29.setVisible(true);
                jLabel29.setText("The room is already booked at the selected time on this date.");
            }
        } 
        else {
            jLabel29.setVisible(true);
            jLabel29.setText("Room with ID " + roomID + " not found.");
}        
    }//GEN-LAST:event_jButton2ActionPerformed

